package day13;

public class DataTypesConcepts {
    public static void main(String[] args) {

        //byte [1 byte =8 bits, range : -128 to +127]
        byte b1 = -128;
        byte b2 = 127;

        //short [2 bytes = 16 bits, range: -32768 to 32767]
        short s1 = -32768;
        short s2 = 32767;

        //int  [4 bytes = 32 bits, range: -2147483648 to 2147483647]
        int i1 = -2147483648;
        int i2 = 2147483647;

        //long [8 bytes =64 bits, range: -9223372036854775808 to 9223372036854775807]
        long l1 =-9223372036854775808L;
        long l2 =9223372036854775807L;

    }
}
