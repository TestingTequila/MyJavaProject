package day18;

public class ArraysConcepts2 {
    public static void main(String[] args) {
        //Kerrie, Jason, Lee, Justin, Ben, Rose [group of values & of same data type]

        String[] myStudents = {"Kerrie", "Jason", "Lee", "Justin", "Ben", "Rose"};

        System.out.println(myStudents[0]);//Kerrie
        System.out.println(myStudents[1]);//Jason
        System.out.println(myStudents[2]);//Lee
        System.out.println(myStudents[3]);//Justin
        System.out.println(myStudents[4]);//Ben
        System.out.println(myStudents[5]);//Rose

        System.out.println("==================================");
        System.out.println(myStudents.length); //6

    }
}
